#include<iostream>
using namespace std;
int main() {

	for (int i = 70; i < 81; i++)
		cout << i << "  ";
	cout << endl;
	for (int j = 100; j < 301; j += 20)
		cout << j << "  ";
	cout << endl;
	for (int k = 25; k > -26; k -= 5)
		cout << k << "  ";
	cout << endl;
	for (int l = 10000; l > 9; l /= 5)
		cout << l << "  ";
	cout << endl;
}
