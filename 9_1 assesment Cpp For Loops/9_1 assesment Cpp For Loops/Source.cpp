using namespace std;
#include<iostream>

int main() {
	cout << endl;
	cout << endl;
	for (int i = 5; i <= 50; i += 3) {
		cout << i << "  ";
	}
	cout << endl;
	cout << endl;
	for (int j = 80; j >= 10; j -= 2) {
		cout << j << "  ";
	}
	cout << endl;
	cout << endl;
	for (int k = 2; k <= 250; k *= 5) {
		cout << k << "  ";
	}
	cout << endl;
	cout << endl;
}